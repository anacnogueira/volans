            <b><br>
            </b> <p><b>1- At&eacute; quantos quilos s&atilde;o permitidos para 
              a bagagem de m&atilde;o? </b><br>
              At&eacute; 5kg para bagagem de m&atilde;o. O volume deve medir no 
              m&aacute;ximo 115cm (soma de largura, altura e profundidade).</p>
            <b><br>
            </b> <p><b>2- E para a bagagem que vai no por&atilde;o? </b><br>
              Para bagagens despachadas no por&atilde;o, cada passageiro adulto 
              tem direito a at&eacute; 2 malas com peso total somado de 23 Kg. 
              Crian&ccedil;a at&eacute; 2 anos incompletos viaja gratuitamente, 
              sem direito a bagagem, desde que viaje no colo de uma pessoa maior 
              de 12 anos, que a esteja acompanhando. Crian&ccedil;a entre 2 e 
              12 anos incompletos t&ecirc;m direito a at&eacute; 2 malas com peso 
              total somado de 11,5 Kg de bagagem. A bagagem que exceder a franquia 
              permitida ser&aacute; considerada como excesso, sendo cobrado 0,5% 
              da tarifa econ&ocirc;mica normal (Y) do trecho, por Kg em excesso 
              e poder&aacute; ter que ser despachada como carga desacompanhada 
              em um pr&oacute;ximo v&ocirc;o.</p>
            <b><br>
            </b> <p><b>3- Se j&aacute; tiver feito check-in, posso alterar o meu 
              v&ocirc;o?</b><br>
              Sim, mas primeiro ter&aacute; que anular o Check-in j&aacute; efectuado 
              para o voo inicial, contactando o balc&atilde;o de Check-in designado 
              para o voo.<br>
              Se optou pelo servi&ccedil;o de Check-in telef&ocirc;nico, e caso 
              n&atilde;o possua bagagem de por&atilde;o, poder&aacute; contactar 
              este servi&ccedil;o para anular o Check-in.</p>
            <b><br>
            </b> <p><b>4- Quais s&atilde;o as formas de pagamento aceitas?</b><br>
              No Balc&atilde;o ou na sua Ag&ecirc;ncia de Viagens s&atilde;o aceites 
              todas as formas de pagamento.<br>
              Se optar pelas Reservas Online ou pelo Call Center apenas poder&aacute; 
              pagar com o seu cart&atilde;o de cr&eacute;dito Visa, MasterCard, 
              Dinners Club e American Express.</p>
            <b><br>
            </b> <p><b>5- Posso mudar minha reserva/bilhete para outra companhia?</b><br>
              Pode alterar a sua reserva e o seu E-Ticket (desde que a tarifa 
              o permita), de uma maneira geral at&eacute; uma hora antes da partida 
              (2 horas para voos intercontinentais).<br>
              Deve contactar pelo telefone um dos nossos Call Centers, ou dirigir-se 
              pessoalmente a um dos nossos Balc&otilde;es de atendimento.</p>
            <p><br>
              <b>6- Quais os documentos necess&aacute;rios no check-in?</b><br>
              Basta apresentar um documento de identifica&ccedil;&atilde;o com 
              fotografia, emitido pelo Governo: Bilhete de Identidade, ou Passaporte.</p>
            <p><b><br>
              7- Para os destinos internacionais, que documento apresentar?</b><br>
              Al&eacute;m dos documentos habituais tais como passaporte, vistos 
              ou outros; poder&aacute; ser-lhe solicitado um recibo/itiner&aacute;rio 
              da sua viagem.<br>
              Pode obt&ecirc;-lo junto dos nossos Balc&otilde;es, ou solicitar 
              a um dos nossos Call Centers que lhe seja enviado o mesmo atrav&eacute;s 
              de fax ou E-mail.</p>
            <p><b><br>
              8- Se comprar pelo site ou por telefone posso receber um recibo?</b><br>
              - Podemos enviar-lhe por Fax ou E-mail um recibo, bem como um itiner&aacute;rio 
              com todas as informa&ccedil;&otilde;es sobre a sua viagem.<br>
              - Pode tamb&eacute;m a qualquer momento (mesmo depois de j&aacute; 
              ter iniciado a sua viagem), obter um recibo/itiner&aacute;rio junto 
              de um dos nossos Balc&otilde;es.<br>
              - Nas vendas ao telefone e nas Reservas Online, poder&aacute; solicitar 
              um recibo para efeitos de IRS, que lhe ser&aacute; enviado por correio 
              para a morada que indicar.</p>
            <br> <p><b>9- Como funciona a compra - online?</b><br>
              Na compra-online os tradicionais coupons de v&ocirc;o em papel s&atilde;o 
              substitu&iacute;dos por coupons &quot;virtuais&quot;.<br>
              Esta facilidade n&atilde;o altera os procedimentos de reserva de 
              lugares nem de pagamento, oferecendo ainda a vantagem de n&atilde;o 
              ter de se deslocar a um dos nossos balc&otilde;es para adquirir 
              o seu bilhete. <br>
              Basta apenas telefonar ou efetuar a sua reserva no site Online.<br>
              O registro do seu bilhete fica guardado na Base de Dados da Volans, 
              e a informa&ccedil;&atilde;o encontra-se acess&iacute;vel em qualquer 
              dos nossos balc&otilde;es ou servi&ccedil;os de atendimento.</p>
