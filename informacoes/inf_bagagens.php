            <p>&nbsp;</p>
            <p><strong>1. Identifica&ccedil;&atilde;o de Bagagens</strong></p>
            <p>&nbsp;</p>
            <p>Para facilitar a identifica&ccedil;&atilde;o de sua bagagem deixe 
              algum sinal externo de f&aacute;cil visualiza&ccedil;&atilde;o, 
              assim quando ela vier pela esteira ser&aacute; rapidamente localizada. 
              Identifique sua bagagem externa e internamente com dados pessoais 
              como nome, telefone e endere&ccedil;o, inclusive um para contato 
              no destino, assim ser&aacute; poss&iacute;vel localizar o passageiro 
              se necess&aacute;rio.</p>
            <p>&nbsp;</p>
            <p><strong>2. A Bagagem de M&atilde;o</strong></p>
            <p>&nbsp;</p>
            <p>A bagagem de m&atilde;o deve ter no m&aacute;ximo 5 kg e deve possuir 
              no m&aacute;ximo 115 cm de dimens&atilde;o total, determinado pela 
              soma da altura, comprimento e largura.<br>
              &Eacute; limitada a uma bagagem de m&atilde;o por passageiro.<br>
              A bagagem deve ser apresentada no check-in da Volans e estando dentro 
              dos padr&otilde;es estabelecido receber&aacute; uma etiqueta de 
              identifica&ccedil;&atilde;o que dever&aacute; ser mantida at&eacute; 
              o desembarque no aeroporto.<br>
              Se por alguma raz&atilde;o a bagagem de m&atilde;o n&atilde;o corresponder 
              &agrave;s normas estabelecidas, ela ser&aacute; encaminhada para 
              o por&atilde;o da aeronave.</p>
            <p>&nbsp;</p>
            <p><strong>3. Bagagem no Por&atilde;o da Aeronave</strong></p>
            <p>&nbsp;</p>
            <p>Para bagagens despachadas no por&atilde;o, cada passageiro adulto 
              tem direito a at&eacute; 2 malas com peso total somado de 23 Kg. 
              Crian&ccedil;a at&eacute; 2 anos viaja gratuitamente, sem direito 
              a bagagem, desde que viaje no colo de uma pessoa maior de 12 anos, 
              que a esteja acompanhando. Crian&ccedil;a entre 3 e 12 anos tem 
              direito a at&eacute; 2 malas com peso total somado de 11,5 Kg.</p>
            <p>&nbsp;</p>
            <p><strong>4. Excesso de Bagagem</strong></p>
            <p>&nbsp;</p>
            <p>Para cada quilo excedente &eacute; acrescido 0,5% da tarifa do 
              trecho determinado.<br>
              Em v&ocirc;os com conex&atilde;o as bagagens s&atilde;o repesadas 
              para verificar se o peso de pe&ccedil;as compradas no local, n&atilde;o 
              fizerem a bagagem exceder o peso, caso ocorra excesso ser&aacute; 
              cobrada multa estipulada acima.<br>
              Para Cadeira de Rodas e C&atilde;o Guia n&atilde;o &eacute; cobrado 
              excesso de peso desde que viajem junto com o dependente.<br>
              Instrumentos musicais de at&eacute; 158 cm obtidos somando o comprimento, 
              largura e altura, ser&atilde;o inclu&iacute;dos na franquia do passageiro 
              e n&atilde;o caracterizam excesso de peso. Estes instrumentos devem 
              ser embalados e dependendo do tamanho ser&atilde;o encaminhados 
              para o por&atilde;o da aeronave.</p>
            <p>&nbsp;</p>
            <p><strong>5. Bagagens n&atilde;o Permitidas</strong></p>
            <p>&nbsp;</p>
            <p>Existem objetos e produtos que n&atilde;o podem ser transportados 
              em aeronaves por motivos de seguran&ccedil;a. Segue a lista destes 
              produtos:</p>
            <p>- amostras para testes;<br>
              - materiais magn&eacute;ticos, oxidantes, radiativos e polimeriz&aacute;veis;<br>
              - equipamento de laborat&oacute;rio, de perfura&ccedil;&atilde;o, 
              el&eacute;trico e dent&aacute;rio;<br>
              - gases comprimidos;<br>
              - produtos venenosos e corrosivos;<br>
              - explosivos, muni&ccedil;&otilde;es e fogos de artif&iacute;cio;<br>
              - produtos qu&iacute;micos e farmac&ecirc;uticos;<br>
              - l&iacute;quidos e s&oacute;lidos inflam&aacute;veis;<br>
              - bar&ocirc;metros, man&ocirc;metros e fot&ocirc;metros;<br>
              - butano;<br>
              - armas de fogo**;</p>
            <p>&nbsp;</p>
            <p>** Exceto quando portadas por autoridades competentes; ou armas 
              desportivas, desmontadas, embaladas e devidamente acondicionadas, 
              devendo ser transportadas no compartimento de bagagem.<br>
            </p></td>
